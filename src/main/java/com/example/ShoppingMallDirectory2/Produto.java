package com.example.ShoppingMallDirectory2;

public class Produto {
private String hasCode;
private String hasProdName;
private String hasQuantity;
private String StoreName;
private String price;

public String getHasCode() {
	return hasCode;
}
public String getHasProdName() {
	return hasProdName;
}
public String getHasQuantity() {
	return hasQuantity;
}
public String getStoreName() {
	return StoreName;
}
public String getPrice() {
	return price;
}
public void setHasCode(String hasCode) {
	this.hasCode = hasCode;
}
public void setHasProdName(String hasProdName) {
	this.hasProdName = hasProdName;
}
public void setHasQuantity(String hasQuantity) {
	this.hasQuantity = hasQuantity;
}
public void setStoreName(String storeName) {
	StoreName = storeName;
}
public void setPrice(String price) {
	this.price = price;
}

}
